        <h2 class="intro-text text-center">Text</h2>
        <p>To investigate the underlying mechanisms of eye growth and disease development for understanding how the eyeball focuses the light and helps to form a clear image for the vision, by using experimental tools in genetics, molecular and cellular biology, biochemistry, and tissue engineering.</p>  
        <p>Current research topics include:</p>
        <ul>
          <li>Lens development and cataract:  To study what functional roles played by intercellular gap junction channels, genetic factors and signaling molecules in the lens development and cataract formation. This project aims to elucidate new insights about how lens cell morphogenesis and homeostasis are maintained for the lens growth and transparency during development, and to provide innovative approaches for delaying age-related cataracts - a leading cause of blindness in the world.</li>
          <li>Function and regulation of retinal pigment epithelium (RPE): To study how intracellular and intravesicular pH homeostasis is regulated by Na+/H+ exchanges (NHE) in RPE. The goal of this project is to understand how dysfunctional RPE, one of the key factors causing age-related macular degeneration (AMD), leads to retinal degeneration - the death of photoreceptors, and to develop novel methods for maintaining or regenerating RPE. AMD is a leading cause of blindness in the western world including the US.</li>
          <li>Signal transduction in retinal and choroidal endothelial cells: To study the roles of surface receptors of low-density lipoprotein receptor families including Lrp5 and VLDLR in retinal angiogenesis. This project aims to elucidate novel mechanisms controlling vascular endothelial cells in development and diseases. </li>
        </ul>
